    public String getReciveAddressDetail() {
        return reciveAddressDetail;
    }

    public void setReciveAddressDetail(String reciveAddressDetail) {
        this.reciveAddressDetail = reciveAddressDetail;
    }

    public String getReciveName() {
        return reciveName;
    }

    public void setReciveName(String reciveName) {
        this.reciveName = reciveName;
    }

    public String getRecivePhone() {
        return recivePhone;
    }

    public void setRecivePhone(String recivePhone) {
        this.recivePhone = recivePhone;
    }

    public String getRecivePos() {
        return recivePos;
    }

    public void setRecivePos(String recivePos) {
        this.recivePos = recivePos;
    }

    public String getStartPos() {
        return startPos;
    }

    public void setStartPos(String startPos) {
        this.startPos = startPos;
    }
